using System;
using System.Collections.Generic;
using System.Reflection;

namespace Google.Api.Ads.AdWords.Util.Selectors {

  /// <summary>
  /// Base class for all the fields classes in autogenerated code.
  /// </summary>
  public class BaseFields {

    /// <summary>
    /// Gets all columns.
    /// </summary>
    /// <param name="type">The type.</param>
    /// <returns>The list of all columns</returns>
    protected static Field[] GetAllFields(Type type) {
      List<Field> retval = new List<Field>();
      FieldInfo[] fields = type.GetFields(BindingFlags.Static | BindingFlags.Public |
          BindingFlags.FlattenHierarchy);
      foreach (FieldInfo field in fields) {
        if (field.DeclaringType == typeof(Field)) {
          Field f = (Field) field.GetValue(null);
          retval.Add(f);
        }
      }
      return retval.ToArray();
    }
  }
}